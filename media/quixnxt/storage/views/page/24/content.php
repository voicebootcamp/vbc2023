                        <h1> <span> VII - Cisco Video Infrastructure Implementation </span> </h1>
            
</div>
</div>







<div class="qx-element-wrap">
    <div
     id="qx-text-h0nt5"      class="qx-element qx-element-text-v2"         
    
     
    >
                        <div class="course-title">
<div class="course-title">
<div class="course-title" style="text-align: justify;">This course is designed to teach technicians to install/configure, operate and maintain core visual communication components for both premise and cloud-based video and TelePresence solutions. Cisco video/UC infrastructure including Expressway (or VCS), Unified Communications Manager (UCM), Cisco Meeting Server (CMS), TelePresence Server, Conductor and TMS will be configured to work in premise based solutions as well as with Cisco Spark and Cisco WebEx cloud solutions. Video Infrastructure Implementation (VII) v2017 replaces the Implementing Cisco TelePresence Video Solutions (VTVS2) course to help students prepare for the The Cisco Video Infrastructure Implementation (VII) exam (500-710).</div>
