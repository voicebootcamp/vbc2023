              <a class="qx-accordion-title" href="#">
                                <span class="wrap-accordion-title">WHAT IS INCLUDED?</span>
              </a>
              <div class="qx-accordion-content" aria-hidden="false">
                <ul>
<li>5 Days Instructor-led Training</li>
<li>Free Retake - Up to 2 Years</li>
<li>Attend Online or Onsite</li>
<li>Full Access to Study Kits</li>
<li>Email Support from Instructor</li>
</ul>
              </div>

                          </li>
                                              
                                                  
            
                            
                        <li class="qx-element-accordion-item  item1">
              <a class="qx-accordion-title" href="#">
                                <span class="wrap-accordion-title">WHAT YOU WILL LEARN?</span>
              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <ul>
<li>
<p>Upon completing this course, students will be able to meet these objectives:</p>
</li>
<li>Navigate CCE configuration and scripting tools</li>
<li>Configure a Dialed Number, Call Type and Media Routing Domain</li>
<li>Build a basic ICM script</li>
<li>Configure Agents and Skill Groups</li>
<li>Configure basic IVR functionality</li>
<li>Implement Attributes and Precision Queues</li>
<li>Configure RONA using CCE configuration tools</li>
<li>Configure and populate an Agent Team and primary Supervisor</li>
<li>Improve Agent efficiency through Finesse enhancements</li>
<li>Build and test a basic VXML application</li>
<li>Implement Roles, Departments and Business Hours</li>
<li>Run CUIC Reports using the Reporting tool</li>
</ul>
              </div>

                          </li>
                                              
                                                  
            
                            
                        <li class="qx-element-accordion-item  item2">
              <a class="qx-accordion-title" href="#">
                                <span class="wrap-accordion-title">COURSE & LAB OUTLINE</span>
              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <p>Course Outline</p>
<p><strong>Module 1 – Cisco Unified Contact Center Review</strong></p>
<ul>
<li>Contact Center Basics</li>
<li>CCE Components and Architecture</li>
<li>Call Flow</li>
<li>CCE Access Tools</li>
<li>Discovery 01-1: Navigating CCE Discovery Architecture and Components</li>
<li>Discovery 01-2: Exploring ICM Configuration Tools</li>
</ul>
<p><strong>Module 2 – Deploying Basic Call Settings</strong></p>
<ul>
<li>Media Routing Domains</li>
<li>Call Types</li>
<li>Dialed Numbers</li>
<li>Discovery 02-1: Administering ICM Dialed Numbers and Call Types</li>
</ul>
<p><strong>Module 3 – Building a Basic Cisco Unified Contact Center Enterprise Script</strong></p>
<ul>
<li>Introduction to Script Editor</li>
<li>Use Script Editor Nodes</li>
<li>Understand Variables</li>
<li>Schedule Scripts</li>
<li>Manage Additional ICM Scripting Tools</li>
<li>Discovery 3-01 Prepare a Basic Label Script</li>
<li>Discovery3-02 Use ICM Tools for ICM Scripts</li>
</ul>
<p><strong>Module 4 – Configure Basic Agent Functionality</strong></p>
<ul>
<li>Introduce Agent Functionality</li>
<li>Configure Agent Desk Settings</li>
<li>Configure Skill Groups and Skill Targets</li>
<li>Configure an Agent</li>
<li>Configure Agent Targeting Rules</li>
<li>Build an Agent Routing ICM Script</li>
<li>Prepare Agent Logon</li>
<li>Discovery 4-01 Configure ICM for Basic Agent and Skill Group Functionality</li>
<li>Discovery 4-02 Configure CUCM for Agent Functionality</li>
<li>Discovery 4-03 Testing Basic Skill Group Functionality in an ICM Script</li>
</ul>
<p><strong>Module 5 – Configuring Basic Call Treatment and Queuing</strong></p>
<ul>
<li>Media Server and Files</li>
<li>ECC Variables</li>
<li>Microapps</li>
<li>Play Media Microapp</li>
<li>Get Digits Microapp</li>
<li>Menu Microapp</li>
<li>Play Data Microapp</li>
<li>Get Speech Microapp</li>
<li>Capture Microapp</li>
<li>Scripting with Microapps</li>
<li>Discovery 5-01 Media Files and Variables in ICM Scripting</li>
<li>Discovery 5-02 Basic IVR Scripting with MicroApps</li>
</ul>
<p><strong>Module 6 – Implementing Precision Routing</strong></p>
<ul>
<li>Exploring the Basics</li>
<li>Migration Path</li>
<li>Skill Groups vs. Precision Queues</li>
<li>Configuring Attributes and Precision Queues</li>
<li>Precision Routing Sample Scenario</li>
<li>Discovery 6-01 Configure and Implement Precision Routing</li>
</ul>
<p><strong>Module 7 – Configuring RONA Support</strong></p>
<ul>
<li>RONA Function Overview</li>
<li>RONA Time-out Considerations</li>
<li>RONA Script Logic</li>
<li>Discovery 7-01 Configuring RONA</li>
</ul>
<p><strong>Module 8 – Configuring Agent Teams and Supervisors</strong></p>
<ul>
<li>Agent Roles</li>
<li>Supervisor</li>
<li>Team Function</li>
<li>Discovery 8-01 Configure Agent Teams and Supervisors</li>
</ul>
<p><strong>Module 9 – Administering the Cisco Finesse Desktop</strong></p>
<ul>
<li>Finesse Server Integration</li>
<li>Custom Call Variable Layouts</li>
<li>Phone Books</li>
<li>Reason Codes</li>
<li>Workflows</li>
<li>Discovery 9-01 Finesse Administration</li>
</ul>
<p><strong>Module 10 – Implementing VXML Applications</strong></p>
<ul>
<li>VXML Overview</li>
<li>CCE VXML Architecture and Logic Flow</li>
<li>Build Basic Call Studio Project</li>
<li>Deploy Project, Verify Config</li>
<li>CCE Scripting for External VXML Applications</li>
<li>Discovery 10-1: VXML Server Configuration and Call Studio Installation</li>
<li>Discovery 10-2: Create and Deploy a Cisco Unified Call Studio Project</li>
<li>Discovery 10-3: Integrate VXML Applications with a CCE Script</li>
</ul>
<p><strong>Module 11 – Configuring Roles, Departments and Business Hours</strong></p>
<ul>
<li>Introducing Roles</li>
<li>Configuring CCE Administrators</li>
<li>Configuring Departments</li>
<li>Defining Business Hours</li>
<li>Discovery 11-01 Configuring Roles, Departments and Business Hours</li>
</ul>
<p><strong>Module 12 – Running Unified CC Enterprise Reports with Unified IC</strong></p>
<ul>
<li>Introduce and Navigate CUIC</li>
<li>Reporting Touch Points</li>
<li>Access CUIC Stock Reports</li>
<li>Create Custom Dashboards</li>
<li>Discovery 12-01 Reporting</li>
</ul>
              </div>

