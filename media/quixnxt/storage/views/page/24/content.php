              <div class="qx-accordion-content" hidden aria-hidden="true">
                <p>This course will provide you with the necessary knowledge and skills to implement a complete Cisco&nbsp;TelePresence solution, including TelePresence endpoints, integration of premise with cloud solutions,&nbsp;meeting solutions, and management software. It describes Cisco business TelePresence solutions and&nbsp;enables students to implement and troubleshoot basic and advanced TelePresence network solutions.&nbsp;</p>
<p>Upon completion of this course, you will be able to:</p>
<ul>
<li>Define Cisco Business TelePresence Solutions components and architectures</li>
<li>Implement Cisco TelePresence Video Communication Server (Expressway) for basic and advanced video networks</li>
<li>Implement Cisco TelePresence Management Suite (TMS)</li>
<li>Configure Cisco Meeting Server Features</li>
<li>Define integration of Cisco Collaboration Premise Solutions with Cisco Cloud, including Cisco Spark,</li>
<li>Cisco Spark Hybrid, and WebEx</li>
</ul>
