                                <span class="wrap-accordion-title">WHAT YOU WILL LEARN?</span>
              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <p>Learn to provide Tier 3 support personnel with the tools and processes required to expediently identify and resolve common issues during ongoing Day 2 operations of a successfully deployed PCCE 12.5 solution.</p>
              </div>

                          </li>
                                              
                                                  
            
                            
                        <li class="qx-element-accordion-item  item2">
              <a class="qx-accordion-title" href="#">
                                <span class="wrap-accordion-title">COURSE & LAB OUTLINE</span>
              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <p><strong>Section 1</strong></p>
<p><strong>CCE Flows and Process Review</strong></p>
<ul>
<li>Describe CCE flows and processes required to support and troubleshoot the PCCE deployment.</li>
</ul>
<p><strong>Troubleshooting and Support Methodology&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong></p>
<ul>
<li>Review concepts for a Troubleshooting Methodology and how to apply these methods in a sample scenario.</li>
</ul>
<p><strong>PCCE Component Review &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong></p>
<ul>
<li>Review the components involved in PCCE Deployment and the function of each.</li>
</ul>
<p><strong>PCCE Call Flow Review</strong></p>
<ul>
<li>Review the Call Flow process to discuss the messages and protocols required to support a PCCE Deployment.</li>
</ul>
<p><strong>PCCE Processes&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong></p>
<ul>
<li>Describe the processes required by various CCE Services and how they support the application.</li>
</ul>
<p><strong>Section 2&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong></p>
<p><strong>CCE Diagnostic tools&nbsp;&nbsp;&nbsp;&nbsp;</strong></p>
<ul>
<li>Introduce the many diagnostic tools available to the engineer responsible for troubleshooting a PCCE environment.</li>
</ul>
<p><strong>Diagnostic Framework Suite&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong></p>
<ul>
<li>Introduce the basics of the Diagnostic Framework Suite and its role in CCE troubleshooting.</li>
</ul>
<p><strong>Run Analysis Manager&nbsp;&nbsp;</strong></p>
<ul>
<li>Use the Analysis Manager tool to run a selection of CCE logs.</li>
</ul>
<p><strong>Run Unified System CLI</strong></p>
<ul>
<li>Use the System CLI to run a selection of CCE logs.</li>
</ul>
<p><strong>Run Diagnostic Framework Portico&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong></p>
<ul>
<li>Use the Diagnostic Framework Portico to run a selection of CCE logs.</li>
</ul>
<p><strong>Run Single Pane of Glass (SPOG)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong></p>
<ul>
<li>Use the SPOG interface to run a selection of CCE logs.</li>
</ul>
<p><strong>ICM Command Line Tools&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong></p>
<ul>
<li>Examine ICM Command Line tools and their use in troubleshooting in a CCE environment.</li>
</ul>
<p><strong>Section 3&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong></p>
<p><strong>Troubleshooting CCE&nbsp;&nbsp;&nbsp;</strong></p>
<ul>
<li>Apply troubleshooting tools and techniques to address issues with CCE Certificates, Cisco Finesse, and PCCE Deployment.</li>
</ul>
<p><strong>Troubleshoot Certificates&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong></p>
<ul>
<li>Examine concepts regarding security certificates and discuss the tools and utilities used to ensure their efficacy.</li>
</ul>
<p><strong>Troubleshoot Cisco Finesse&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong></p>
<ul>
<li>Define the protocols, tools, and procedures used to troubleshoot the Cisco Finesse client/server environment.</li>
</ul>
<p><strong>Troubleshoot a PCCE Deployment &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong></p>
<ul>
<li>&nbsp;Examine troubleshooting scenarios and solutions witnessed in building the PCCE lab-only deployment model used for this course.</li>
</ul>
              </div>

                          </li>
                                              
                                                  
            
                            
                        <li class="qx-element-accordion-item  item3">
              <a class="qx-accordion-title" href="#">
                                <span class="wrap-accordion-title">PREREQUISITES</span>
              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <ul>
<li>
<p>Before taking this course, you should have the following knowledge and skills:</p>
</li>
<li>Understanding of networking technologies</li>
<li>Understanding voice and video</li>
<li>Knowledge of Cisco collaboration core technologies</li>
<li>Session Initiation Protocol (SIP) and Extensible Messaging and Presence Protocol (XMPP) signaling protocol fundamentals</li>
<li>Collaboration call control fundamentals of Cisco Unified Communications Manage</li>
</ul>
              </div>

                          </li>
                              </ul>
                
