                        <h1> <span> Implementing and Operating Cisco Collaboration Core Technologies (CLCOR) </span> </h1>
            
</div>
</div>







<div class="qx-element-wrap">
    <div
     id="qx-text-90u3i"      class="qx-element qx-element-text-v2"         
    
     
    >
                        <p style="text-align: justify;"><strong>Implement and deploy core collaboration and networking technologies featuring Cisco Unified Communications Manager.</strong></p>
<p style="text-align: justify;">You will gain the knowledge and skills needed to implement and deploy core collaboration and networking technologies, including infrastructure and design, protocols, codecs, and endpoints, Cisco Internetwork Operating System (IOS) XE gateway and media resources, call control, Quality of Service (QoS), and additional Cisco collaboration applications.</p>
<p style="text-align: justify;">This course also helps you prepare you to take the exam, Implementing Cisco Collaboration Core Technologies (350-801 CLCOR), which is part of the new CCNP Collaboration, CCIE Collaboration, and the Cisco Certified Specialist - Collaboration Core certifications.</p>
            
</div>
</div>













