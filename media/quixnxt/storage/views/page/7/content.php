<p style="text-align: justify;">The&nbsp;<em>ENCOR - Implementing and Operating Cisco Enterprise Network Core Technologies v1.2</em>&nbsp;course gives you the knowledge and skills needed to configure, troubleshoot, and manage enterprise wired and wireless networks. You&rsquo;ll also learn to implement security principles within an enterprise network and how to overlay network design by using solutions such as SD-Access and SD-WAN.</p>
