                        <h1> <span> UCCXD - Deploying Cisco Unified Contact Center Express V12.X </span> </h1>
            
</div>
</div>







<div class="qx-element-wrap">
    <div
     id="qx-text-mdeqp"      class="qx-element qx-element-text-v2"         
    
     
    >
                        <div class="course-title">
<div class="course-title">
<div class="course-title">
<p class="course-short-desc" style="text-align: justify;"><strong>Learn about the Cisco Unified Contact Center Express (Unified CCX) product and its associated client applications and scripting tools.</strong></p>
</div>
<div class="editor">
<p style="text-align: justify;">In this course, you'll gain an understanding of Cisco’s Unified Contact Center Express (Unified CCX) product and all of its associated client applications and scripting tools. You will learn about all of the Unified CCX components, servers, deployment options, and clients. You will learn how to perform capacity planning, choose the correct product features, and install the product.</p>
</div>
</div>
</div>
            
</div>
</div>













