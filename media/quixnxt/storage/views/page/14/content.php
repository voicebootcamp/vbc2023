                        <li class="qx-element-accordion-item  item3">
              <a class="qx-accordion-title" href="#">
                                <span class="wrap-accordion-title">PREREQUISITES</span>
              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <ul>
<li>Internet web browser usability knowledge and general computer usage</li>
<li>Basic understanding of networking technologies</li>
<li>Basic understanding of voice and video</li>
<li>Describe the different codecs and how they are used to transform analogue voice into digital streams</li>
<li>Knowledge of Cisco Internetworking Operation System (Cisco IOS XE) command line</li>
<li>Describe the Cisco Collaboration solutions architecture</li>
<li>Define collaboration and describe the main purpose of key devices in a Cisco collaboration on-premises deployment model</li>
<li>Configure and modify required parameters in Cisco Unified CM including service activation, enterprise parameters, CM groups, time settings, and device pool</li>
<li>Deploy and troubleshoot IP phones via manual configuration within Cisco Unified CM</li>
<li>Describe and configure endpoints and commonly required features</li>
<li>Compare the IP Phone signaling protocols of Session Initiation Protocol (SIP), H.323, Media Gateway Control Protocol (MGCP), and Skinny Call Control Protocol (SCCP)</li>
<li>Analyze traffic patterns and quality issues in converged IP networks supporting voice, video, and data traffic</li>
<li>Define Quality of Service (QoS) and its models</li>
<li>Describe the call setup and teardown process for a SIP device including codec negotiation using Session Description Protocol (SDP) and media channel setup</li>
<li>Manage Cisco Unified CM user accounts (local and via Lightweight Directory Access Protocol [LDAP])</li>
<li>Describe a dial plan and explain call routing in Cisco Unified Communications Manager</li>
<li>Configure dial plan elements within a single site Cisco Unified CM deployment including Route Groups, Local Route Group, Route Lists, Route Patterns, Translation Patterns, Transformations, SIP Trunks, and SIP Route Patterns</li>
<li>Implement basic globalized call routing within a Cisco Unified Communications Manager cluster</li>
<li>Configure calling privileges in Cisco Unified Communications Manager</li>
<li>Implement toll fraud prevention</li>
<li>Implement common endpoint features including call park, softkeys, shared lines, and pickup groups</li>
<li>Implement Public Switched Telephone Network (PSTN) access using Media Gateway Control Protocol (MGCP) gateways</li>
<li>Implement a Cisco gateway for PSTN access</li>
<li>Deploy a simple SIP dial plan on a Cisco Interrupt Service Routine (ISR) gateway to enable access to the PSTN network</li>
<li>Implement and troubleshoot media resources in Cisco Unified Communications Manager</li>
<li>Manage Cisco Unified CM access to media resources available within Cisco Unified CM and Cisco ISR gateways</li>
<li>Describe tools for reporting and maintenance including Unified Reports, Real-Time Monitoring Tool (RTMT), Distributed Resource Scheduler (DRS), and Call Detail Records (CDRs) within Cisco Unified CM</li>
</ul>
