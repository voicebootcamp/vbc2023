<p class="course-short-desc" style="text-align: justify;"><strong>Learn advanced techniques in installing, troubleshooting, and maintaining a single server and single server split deployment of Cisco Meeting Servers.</strong></p>
</div>
<div class="editor">
<p style="text-align: justify;">In this course, you will learn advanced techniques in installing, troubleshooting, and maintaining a single server and single server split deployment of Cisco Meeting Servers.</p>
<p style="text-align: justify;">The recently purchased Acano collaboration hardware and software includes video and audio-bridging technology that allows customers to connect video systems from multiple vendors across both cloud and hybrid environments. Cisco has incorporated Acano collaboration technologies into the new Meeting Server tool.</p>
