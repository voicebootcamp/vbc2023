                        <h1> <span> CCEAA-Cisco Contact Center Enterprise Advanced Administration </span> </h1>
            
</div>
</div>







<div class="qx-element-wrap">
    <div
     id="qx-text-wc3j7"      class="qx-element qx-element-text-v2"         
    
     
    >
                        <div class="course-title">
<div class="course-title">
<p class="course-short-desc"><strong>Cisco Contact Center Enterprise Advanced Administration (CCEAA).</strong></p>
</div>
<div class="editor">
<p style="text-align: justify;">This is a 5-day instructor-led course intended for system engineers and customers who will be deploying and administering advanced Packaged Contact Center Enterprise functionality</p>
<p style="text-align: justify;">The course allows learners to deploy the Unified CCE v12.5 solution including installation, deploying HA and using troubleshooting tools to identify issues with inbound and outbound Contact Center functionality. This course is intended for those installing the Unified CCE solution, or those providing Level 3 solution support. This course also serves as a foundation for the two corresponding UCCE/PCCE Administration courses, but is not a prerequisite. Students will learn enough about CCE scripting in this course to ensure system functionality only. A separate course exists for advanced scripting..</p>
</div>
</div>
            
</div>
</div>













