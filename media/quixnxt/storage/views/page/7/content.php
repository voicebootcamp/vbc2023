<li><span style="font-size: 10pt;">Describe how APs communicate with WLCs to obtain software, configurations, and centralized management</span></li>
<li><span style="font-size: 10pt;">Configure and verify Extensible Authentication Protocol (EAP), WebAuth, and Pre-shared Key (PSK) wireless client authentication on a WLC</span></li>
<li><span style="font-size: 10pt;">Troubleshoot wireless client connectivity issues using various available tools</span></li>
<li><span style="font-size: 10pt;">Troubleshooting Enterprise networks using services such as Network Time Protocol (NTP), Simple Network Management Protocol (SNMP), Cisco Internetwork Operating System (Cisco IOSÂ®) IP Service Level Agreements (SLAs), NetFlow, and Cisco IOS Embedded Event Manager</span></li>
<li><span style="font-size: 10pt;">Explain the use of available network analysis and troubleshooting tools, which include show and debug commands, as well as best practices in troubleshooting</span></li>
<li><span style="font-size: 10pt;">Configure secure administrative access for Cisco IOS devices using the Command-Line Interface (CLI) access, Role-Based Access Control (RBAC), Access Control List (ACL), and Secure Shell (SSH), and explore device hardening concepts to secure devices from less secure applications, such as Telnet and HTTP</span></li>
<li><span style="font-size: 10pt;">Implement scalable administration using Authentication, Authorization, and Accounting (AAA) and the local database, while exploring the features and benefits</span></li>
<li><span style="font-size: 10pt;">Describe the enterprise network security architecture, including the purpose and function of VPNs, content security, logging, endpoint security, personal firewalls, and other security features</span></li>
<li><span style="font-size: 10pt;">Explain the purpose, function, features, and workflow of Cisco DNA Centerâ¢ Assurance for Intent-Based Networking, for network visibility, proactive monitoring, and application experience</span></li>
<li><span style="font-size: 10pt;">Describe the components and features of the Cisco SD-Access solution, including the nodes, fabric control plane, and data plane, while illustrating the purpose and function of the Virtual Extensible LAN (VXLAN) gateways</span></li>
<li><span style="font-size: 10pt;">Define the components and features of Cisco SD-WAN solutions, including the orchestration plane, management plane, control plane, and data plane</span></li>
<li><span style="font-size: 10pt;">Describe the concepts, purpose, and features of multicast protocols, including Internet Group Management Protocol (IGMP) v2/v3, Protocol-Independent Multicast (PIM) dense mode/sparse mode, and rendezvous points</span></li>
<li><span style="font-size: 10pt;">Describe the concepts and features of Quality of Service (QoS), and describe the need within the enterprise network</span></li>
<li><span style="font-size: 10pt;">Explain basic Python components and conditionals with script writing and analysis</span></li>
<li><span style="font-size: 10pt;">Describe network programmability protocols such as Network Configuration Protocol (NETCONF) and RESTCONF</span></li>
<li><span style="font-size: 10pt;">Describe APIs in Cisco DNA Center and vManage</span></li>
