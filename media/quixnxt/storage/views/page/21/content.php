                        <li class="qx-element-accordion-item  item1">
              <a class="qx-accordion-title" href="#">
                                <span class="wrap-accordion-title">WHAT YOU WILL LEARN?</span>
              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <ul>
<li>Planning and deploying a Cisco Unified CCX system version 12.6</li>
<li>Telephony and media configuration for deployment with CUCM</li>
<li>Scripting techniques and best practices</li>
<li>Scripting operations including greeting, prompt and collect, caller input, database queries, and logic</li>
<li>Time-of-day and holiday operations</li>
<li>Queue management and best practices for queuing</li>
<li>Finesse Agent, Finesse Supervisor, and Finesse Administrator</li>
<li>Cisco IP Phone Agent</li>
<li>Session Management</li>
<li>CUIC and real-time reporting</li>
<li>Remote Monitoring</li>
<li>Outbound preview dialing</li>
<li>TTS and ASR</li>
<li>Real-Time Monitoring</li>
<li>Disaster recovery</li>
</ul>
              </div>

                          </li>
                                              
                                                  
            
                            
                        <li class="qx-element-accordion-item  item2">
              <a class="qx-accordion-title" href="#">
                                <span class="wrap-accordion-title">COURSE & LAB OUTLINE</span>
              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <p>1.&nbsp;Cisco Unified CCX</p>
<ul>
<li>Relationship between the Unified CCX platform and the three products it supports</li>
<li>Three Cisco products supported by the Unified CCX engine platform</li>
<li>Unified CCX hardware and software components to include all server types, standby deployments, and scalability&nbsp;</li>
<li>New and improved functions of the three Unified CCX products to include Unified CCX v11.0</li>
</ul>
<p>2.&nbsp;Designing and Ordering</p>
<ul>
<li>Properly size the Unified CCX products by using calculators provided by Cisco&nbsp;</li>
<li>Properly order Unified CCX products using the Unified CCX configuration and ordering tool&nbsp;</li>
<li>Network considerations surrounding a Unified CCX deployment&nbsp;</li>
<li>Using the Solution Reference Network Design for Cisco Unified CCX and Cisco Unified IP IVR and other documents to support a Unified CCX design and deployment</li>
</ul>
<p>3.&nbsp;Installation and Configuration</p>
<ul>
<li>Installing Unified CCX software on all servers in a cluster&nbsp;</li>
<li>Activating and configuring all components in a CCX cluster&nbsp;</li>
<li>Configuring and testing a simple CCX Script Application&nbsp;</li>
<li>Troubleshooting installations using log files</li>
</ul>
<p>4.&nbsp;Unified CCX Editor</p>
<ul>
<li>Navigate the Unified CCX Editor functionality&nbsp;</li>
<li>Creating, deleting, and editing variables&nbsp;</li>
<li>Saving and uploading valid scripts to the repository&nbsp;</li>
<li>Performing the debug process to test a script&nbsp;</li>
<li>Troubleshooting an application and script using trace files</li>
</ul>
<p>5.&nbsp;Basic Unified CCX Editor Steps</p>
<ul>
<li>Creating a simple script workflow to answer a call&nbsp;</li>
<li>Adding comments to identify and explain the script and subsequent groups of steps&nbsp;</li>
<li>Playing voice prompts to share information or instructions with callers&nbsp;</li>
<li>Terminating and ending a call&nbsp;</li>
<li>Validate, save as, upload, refresh, and debug a script</li>
</ul>
<p>6.&nbsp;Caller and System Inputs</p>
<ul>
<li>Collecting information from a caller by presenting a list of choices using the menu step</li>
<li>Collecting digits from a caller by using the Get Digit String step&nbsp;</li>
<li>Getting information from the system to alter the logic of the call flow&nbsp;</li>
<li>Validate, save as, upload, refresh, and debug the script</li>
</ul>
<p>7.&nbsp;Database Access</p>
<ul>
<li>Connecting to a database&nbsp;</li>
<li>Reading and getting information from the database by creating a SQL query within a script step&nbsp;</li>
<li>Referencing database locations&nbsp;</li>
<li>Writing information to a SQL database&nbsp;</li>
<li>Closing the database connection and terminating database resources</li>
</ul>
<p>8.&nbsp;Logical Operations</p>
<ul>
<li>Applying Boolean logic in scripts&nbsp;</li>
<li>Creating and modifying counters&nbsp;</li>
<li>Creating and managing timing loops&nbsp;</li>
<li>Redirecting script logic based on the evaluation of “If” statements&nbsp;</li>
<li>Using call subflows as reusable scripts&nbsp;</li>
<li>Creating specialized prompts</li>
</ul>
<p>9.&nbsp;Caller Transfers</p>
<ul>
<li>Creating day-of-week and time-of-day ranges for different handling&nbsp;</li>
<li>Transferring calls to various extensions as a function of day and time&nbsp;</li>
<li>Getting information associated with a call</li>
<li>Designating calls as completed for reporting&nbsp;</li>
<li>Determining if the caller has hung up&nbsp;</li>
<li>Determining if today is a holiday</li>
</ul>
<p>10.&nbsp;Configuring Unified CCX</p>
<ul>
<li>Cisco Unified CCX product, features, and capabilities&nbsp;</li>
<li>Configuring Unified CCX on the CUCM and CCX server</li>
</ul>
<p>11.&nbsp;Finesse Product Suite</p>
<ul>
<li>Configuring Finesse Agent Desktop and Finesse Supervisor Desktop&nbsp;</li>
<li>Testing the Finesse Agent Desktop and Finesse Supervisor Desktop configurations&nbsp;</li>
<li>Examining the IP Phone Agent</li>
</ul>
<p>12.&nbsp;Unified CCX Script Techniques</p>
<ul>
<li>Defining and using Unified CCX Editor Unified CCX steps&nbsp;</li>
<li>Testing a sample application using the Unified CCX steps in the Unified CCX Editor</li>
</ul>
<p>13.&nbsp;Finesse Administrator</p>
<ul>
<li>Configuring the Unified CCX enterprise data using the Finesse Desktop Administrator&nbsp;</li>
<li>Configuring work flows, personnel, and&nbsp;teams&nbsp;</li>
<li>Creating screen pops and macros&nbsp;</li>
<li>Creating wrap-up codes</li>
</ul>
<p>14.&nbsp;Outbound Dialer and Agent Chat</p>
<ul>
<li>Examine Outbound Dialer</li>
</ul>
<p>15.&nbsp;Session Management</p>
<ul>
<li>Examine Session Management to retrieve information from a previous call</li>
</ul>
<p>16.&nbsp;Media Resource Control Protocol (MRCP) Speech Technologies</p>
<ul>
<li>Examine Nuance ASR and TTS servers</li>
</ul>
<p>17.&nbsp;Reporting</p>
<ul>
<li>Accessing and analyzing real-time reports&nbsp;</li>
<li>Configure Cisco Unified Intelligence Center (CUIC) historical reporting&nbsp;</li>
<li>Accessing and analyzing historical reports</li>
</ul>
<p>18.&nbsp;Servicing and Troubleshooting</p>
<ul>
<li>Traces and logs</li>
</ul>
              </div>

