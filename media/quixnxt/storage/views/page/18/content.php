                        <h1> <span> CCEA – Administering Cisco Contact Center Enterprise </span> </h1>
            
</div>
</div>







<div class="qx-element-wrap">
    <div
     id="qx-text-g7k5h"      class="qx-element qx-element-text-v2"         
    
     
    >
                        <div class="course-title">
<p class="course-short-desc" style="text-align: justify;">The Administering Cisco Unified Contact Center Enterprise is a 5-day course intended for system engineers, administrators, and support engineers responsible for tier one support. This course provides hands-on practice with administrative tools used to perform routine adds, moves, and changes in the inbound contact center environment.</p>
</div>
            
</div>
</div>













