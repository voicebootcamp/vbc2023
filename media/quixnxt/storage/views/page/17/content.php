              </div>

                          </li>
                                              
                                                  
            
                            
                        <li class="qx-element-accordion-item  item1">
              <a class="qx-accordion-title" href="#">
                                <span class="wrap-accordion-title">WHAT YOU WILL LEARN?</span>
              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <ul>
<li>
<p>Upon completing this course, students will be able to meet these objectives:</p>
</li>
<li>Describe the components, protocols, and call flow of PCCE by referencing the discovery platform to prepare for further scripting and configuration activities</li>
<li>Run the CCE Bulk Import utility using the CCE Web Administration tool to develop a base line CCE configuration</li>
<li>Configure an advanced VXML application implementing DB lookup functionality and digit collection using Call Studio and CCE Scripting tools in order to present call data collected from the caller to the Agent desktop</li>
<li>Provision CCE to support CUCM calls to the Contact Center using UCM and CCE configuration tools. This will enable CCE Route Requests from CUCM to support contacts initiated from a CUCM managed device (Gateways, Phones, Line Side IVR Ports). This may be to enable non-Contact Center calls and calls handled by Agents whether existing or new</li>
<li>Access and deploy custom gadgets to the Finesse desktop using the CCE Web Administration tool to further enhance functionality of the Finesse Agent Desktop</li>
<li>Successfully deploy Mobile Agent in a CCE Environment</li>
<li>Successfully deploy Post Call Survey in a CCE Environment</li>
</ul>
              </div>

                          </li>
                                              
                                                  
            
                            
                        <li class="qx-element-accordion-item  item2">
              <a class="qx-accordion-title" href="#">
                                <span class="wrap-accordion-title">COURSE & LAB OUTLINE</span>
              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <p><strong>Module 1 – PCCE Review</strong></p>
<ul>
<li>PCCE Architecture and Components Review</li>
<li>PCCE Protocols Review</li>
<li>PCCE Call Flow Review</li>
<li>PCCE Access Tools Review</li>
<li>Discovery 01-1: Review Discovery</li>
<li>Discovery 01-2: Navigating CCE Discovery Architecture and Components</li>
</ul>
<p><strong>Module 2 – Introducing Bulk Import Tools</strong></p>
<ul>
<li>Use the PCCE Bulk Import Tool</li>
<li>Use Bulk Import Templates</li>
<li>Discovery 02-1: Importing Bulk Data</li>
</ul>
<p><strong>Module 3 – Configuring Advanced Scripting and CCE Data Exchange</strong></p>
<ul>
<li>Design for Advanced Scripting</li>
<li>CCE Data Exchange</li>
<li>Using Call Studio</li>
<li>Implement Database Lookup using VXML</li>
<li>Collect Response from the Caller</li>
<li>Invoking Call Studio Applications with CCE Routing Scripts</li>
<li>Discovery 3-01 Creating VXML Application using Call Studio</li>
<li>Discovery3-02 Configure Precision Queues</li>
<li>Discovery 3-03 Creating CCE Routing Script</li>
<li>Discovery 3-04 Customizing Finesse Desktop</li>
<li>Discovery 3-05 Testing your Call Flow</li>
</ul>
<p><strong>Module 4 – CUCM Initiated Call Flows</strong></p>
<ul>
<li>Understand Transfer Types and CVP Call Flow Models</li>
<li>Describe Subsequent Transfers</li>
<li>Perform UCM Configurations for Transfers</li>
<li>Configure CUCM as Routing Client and Agent Transfers</li>
<li>Discovery 4-01 Configure CUCM as Routing Client and Agent Transfers</li>
</ul>
<p><strong>Module 5 – Using Gadgets to Customize the Finesse Desktop</strong></p>
<ul>
<li>Obtain Finesse Custom Gadgets</li>
<li>Deploy Finesse Custom Gadgets</li>
<li>Discovery 5-01 Deploying Finesse Gadgets</li>
</ul>
<p><strong>Module 6 – Implementing Mobile Agent</strong></p>
<ul>
<li>Examining Mobile Agent Functionality</li>
<li>Identify Mobile Agent Architecture and Components</li>
<li>Configuring Mobile Agent</li>
<li>Logging in as Mobile Agent</li>
<li>Planning Mobile Agent Design and Integration</li>
<li>Discovery 6-01 Implementing Mobile Agent</li>
</ul>
<p><strong>Module 7 – Implementing Post Call Survey</strong></p>
<ul>
<li>Examining Post Call Survey Functionality</li>
<li>Configuring Post Call Survey</li>
<li>Reporting Considerations for Post Call Survey</li>
<li>Discovery 7-01 Implementing Post Call Survey</li>
</ul>
              </div>

                          </li>
                                              
                                                  
            
                            
                        <li class="qx-element-accordion-item  item3">
              <a class="qx-accordion-title" href="#">
                                <span class="wrap-accordion-title">PREREQUISITES</span>
              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <ul>
<li>Basic knowledge of networking (Windows A/D, SQL) and components (servers, routers, switch) is helpful but not required.</li>
<li>Working knowledge of Unified Communications Manager and voice gateways.</li>
<li>Basic understanding of Cisco Unified Contact Center Enterprise architecture and operation.</li>
</ul>
              </div>

                          </li>
                              </ul>
                
