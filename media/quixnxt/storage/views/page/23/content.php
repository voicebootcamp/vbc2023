                        <li class="qx-element-accordion-item  item2">
              <a class="qx-accordion-title" href="#">
                                <span class="wrap-accordion-title">COURSE & LAB OUTLINE</span>
              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <p><b>Course outline</b></p>
<p><strong>1. Reviewing Cisco Meeting Server Intermediate</strong></p>
<ul>
<li>Component parts of a Cisco Meeting Solution</li>
<li>Configuration steps for a Cisco Meeting Solution</li>
</ul>
<p><strong>2. APIs</strong></p>
<ul>
<li>Purpose of APIs</li>
<li>Benefits of APIs</li>
<li>Types of APIs</li>
<li>Function of the GET, POST, PUT, and DELETE commands</li>
</ul>
<p><strong>3. Configuring Software with an API</strong></p>
<ul>
<li>How a user can interact with software using an API</li>
<li>Download and install Chrome Postman</li>
<li>Other API software</li>
<li>Download the Cisco Meeting Server API guide</li>
</ul>
<p><strong>4. Configuring Spaces with the Cisco Meeting Server API</strong></p>
<ul>
<li>Cisco Meeting Server API structure</li>
<li>Use Chrome Postman for information on calls and spaces with the GET command</li>
<li>Use Chrome Postman to post a new call space with the POST command</li>
<li>Use Chrome Postman to place changes in a space, including adding a member, with the PUT command</li>
<li>Use Chrome Postman to delete a space with the DELETE command</li>
</ul>
<p><strong>5. Customization Configuration</strong></p>
<ul>
<li>Create and modify a user profile and assign users<br />Create and modify a dual-tone multifrequency (DTMF) profile and apply to a user profile<br />Modify the interactive voice response (IVR), color scheme, and background</li>
</ul>
<p><strong>6. Planning a Resilient and Scalable Cisco Meeting Server Deployment</strong></p>
<ul>
<li>Resilient server solutions</li>
<li>Scalable server solutions</li>
<li>Geographically dispersed configurations and GeoDNS</li>
<li>Domain Name System (DNS) records required for a resilient and scalable solution</li>
<li>Certificate requirements for a resilient and scalable solution</li>
</ul>
<p><strong>7. Configuring a Database Cluster</strong></p>
<ul>
<li>Relationship between cluster master and cluster slaves in a database cluster</li>
<li>Certificate requirements for a secure database cluster</li>
<li>Configure the certificates for a database cluster</li>
<li>Configure a database cluster</li>
</ul>
<p><strong>8. Configuring a Call Bridge Cluster</strong></p>
<ul>
<li>Relationship between the Call Bridge and the Database</li>
<li>How cross-cluster spaces behave</li>
<li>Configure certificates for Call Bridge clustering</li>
<li>Storage of configuration when entering with the API and the individual web interfaces for Lightweight Directory Access Protocol (LDAP) and Call Bridge clustering</li>
<li>Configure a Call Bridge Cluster</li>
<li>Connect XMPP server to multiple Call Bridges</li>
</ul>
<p><strong>9. Configuring Load Balancers and Trunks</strong></p>
<ul>
<li>Relationship between the XMPP server, Trunk and Load Balancer</li>
<li>Configure certificates for multiple trunks and load balancers</li>
<li>Configure multiple Trunks to multiple Load Balancers</li>
</ul>
<p><strong>10. Configuring Multiple Web Bridges</strong></p>
<ul>
<li>Relationship between multiple Web Bridges and Call Bridges</li>
<li>Internal and external DNS records support for both internal and external Web Bridges</li>
<li>Configure certificates for multiple Web Bridges</li>
<li>Configure multiple Web bridges</li>
<li>Connect multiple Call Bridges to multiple Web Bridges</li>
</ul>
<p><strong>11. Configuring Multiple TURN Servers</strong></p>
<ul>
<li>Relationship between multiple TURN servers and multiple Call Bridges</li>
<li>Configure certificates for multiple TURN servers</li>
<li>Configure multiple TURN servers</li>
<li>Connect multiple Call Bridges to multiple TURN servers</li>
</ul>
<p><strong>12. Configuring Multiple Recorders</strong></p>
<ul>
<li>Features of the recording capability</li>
<li>Software, hardware, and licensing requirements for recording</li>
<li>DNS records</li>
<li>Configure the Recorder</li>
<li>Relationship between multiple Recorders and multiple Call Bridges</li>
<li>Configure a Call Bridge to use a Recorder</li>
<li>Configure certificates for multiple Recorders servers</li>
<li>Configure multiple Recorder servers</li>
<li>Connect multiple Call Bridges to multiple Recorder servers</li>
</ul>
<p><strong>13. Integrating with a Resilient and Scalable Cisco Meeting Server Deployment</strong></p>
<ul>
<li>Integration with Cisco TelePresence Video Communication Server (VCS) or Cisco Unified Communications Manager and multiple Call Bridges</li>
<li>Integration with Cisco Expressway technology and multiple Call Bridges</li>
<li>Integration with Microsoft Skype for Business and multiple Call Bridges</li>
<li>Integration with Cisco TelePresence Management Suite and multiple Call Bridges</li>
</ul>
<p><strong>14. Deploying an H.323 Gateway</strong></p>
<ul>
<li>Cisco preferred architecture for H.323 and Session Initiation Protocol (SIP) interoperability</li>
<li>Functionality of the Cisco Meeting Server H.323 gateway</li>
<li>Configuration the Cisco Meeting Server H.323 gateway</li>
</ul>
<p><strong>15. Multitenancy Options</strong></p>
<ul>
<li>Purpose of the multitenancy capabilities</li>
<li>Options for multitenancy capabilities on the Cisco Meeting Server</li>
</ul>
<p><strong>16. Customization Options</strong></p>
<ul>
<li>Options available to customize Cisco Meeting Apps</li>
<li>License keys required for customization</li>
<li>Web Server requirements for customization</li>
<li>Options available to customize recorded messaging</li>
<li>Customization options available for invitation text</li>
</ul>
<p><strong>Lab Outline</strong></p>
<ul>
<li>Lab 1: Single-Server Deployment</li>
<li>Lab 2: API Introduction</li>
<li>Lab 3: Customization</li>
<li>Lab 4: Database Cluster Configuration</li>
<li>Lab 5: Call Bridge Cluster Configuration</li>
<li>Lab 6: Multiple-Trunk and Load-Balancer Configuration</li>
<li>Lab 7: Multiple Web Bridge Configuration</li>
<li>Lab 8: Multiple TURN Server Configuration</li>
<li>Lab 9: Multiple Recorder Configuration</li>
<li>Lab10: Integrating with VCS and Microsoft Skype for Business in a Multiple Call Bridge Installation</li>
</ul>
