<li>Implement DIA and Cisco SD-WAN Cloud OnRamp options</li>
<li>Describe Cisco SD-WAN security components and integration</li>
<li>Describe how to design pure and hybrid Cisco SD-WAN solutions, as well as how to perform a migration to Cisco SD-WAN</li>
<li>Describe Cisco SD-WAN Day-2 operations, such as monitoring, reporting, logging, troubleshooting, and upgrading</li>
<li>Describe Cisco SD-WAN support for multicast</li>
