<div class="course-title">
<div class="course-title">
<p class="course-short-desc" style="text-align: justify;"><strong>Learn advanced scripting techniques using complex script steps to resolve sophisticated contact center requirements.</strong></p>
</div>
<div class="editor">
<p style="text-align: justify;">Advanced Contact Center Express Scripting Labs (ACCXSL) is a 5-day instructor-led, lab-intensive course intended for experienced Unified Contact Center Express (CCX) administrators who need in-depth knowledge advanced scripting techniques using complex script steps to resolve sophisticated contact center requirements. This course addresses the steps in the Script Editor pallet as well as scripting techniques involving databases, HTTP, XML, VXML, Java Objects, and Finesse Administration.</p>
<p style="text-align: justify;">This course is intended to be a follow-on course to UCCX. ACCXSL introduces updated best practices, sophisticated steps, and modern concepts for deploying advanced contact center applications and techniques.</p>
<p style="text-align: justify;">The class utilizes Cisco Unified Contact Center Express and highlights new application features and services. Sunset Learning has deployed Unified CCX labs on the latest UCS platforms using VMWare ESXi and vSphere technology.</p>
