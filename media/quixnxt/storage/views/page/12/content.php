              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <ul>
<li>Describe the Cisco Collaboration solutions architecture</li>
<li>Compare the IP Phone signaling protocols of Session Initiation Protocol (SIP), H323, Media Gateway Control Protocol (MGCP), and Skinny Client Control Protocol (SCCP)</li>
<li>Integrate and troubleshoot Cisco Unified Communications Manager with LDAP for user synchronization and user authentication</li>
<li>Implement Cisco Unified Communications Manager provisioning features</li>
<li>Describe the different codecs and how they are used to transform analogue voice into digital streams</li>
<li>Describe a dial plan, and explain call routing in Cisco Unified Communications Manager</li>
<li>Implement Public Switched Telephone Network (PSTN) access using MGCP gateways</li>
<li>Implement a Cisco gateway for PSTN access</li>
<li>Configure calling privileges in Cisco Unified Communications Manager</li>
<li>Implement toll fraud prevention</li>
<li>Implement globalized call routing within a Cisco Unified Communications Manager cluster</li>
<li>Implement and troubleshoot media resources in Cisco Unified Communications Manager</li>
<li>Describe Cisco Instant Messaging and Presence, including call flows and protocols</li>
<li>Describe and configure endpoints and commonly required features</li>
<li>Configure and troubleshoot Cisco Unity Connection integration</li>
<li>Configure and troubleshoot Cisco Unity Connection call handlers</li>
<li>Describe how Mobile Remote Access (MRA) is used to allow endpoints to work from outside the company</li>
<li>Analyze traffic patterns and quality issues in converged IP networks supporting voice, video, and data traffic</li>
<li>Define QoS and its models</li>
<li>Implement classification and marking</li>
<li>Configure classification and marking options on Cisco Catalyst switches</li>
</ul>
