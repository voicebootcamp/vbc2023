                        <p style="text-align: justify;">In this course, you will learn how to plan, design, configure, maintain, and troubleshoot an integrated communications solution at an organization using Microsoft Teams. The course will cover Teams Phone with Calling Plans, Direct Routing, and Operator Connect, in addition to Teams devices, audio/video conferencing, and voice migration. Students will learn troubleshooting methodologies and how to resolve common telephony and voice problems.</p>
<p style="text-align: justify;">The Microsoft Teams Voice Engineer plans, designs, configures, maintains, and troubleshoots an integrated communications solution at an organization. The Microsoft Teams Voice Engineer must be able to translate business requirements into technical architecture and designs for communication solutions. The Microsoft Teams Voice Engineer is familiar with telecommunication technologies and has experience in Microsoft Teams, Microsoft 365, and PowerShell. They must be able to deploy and configure Microsoft Teams Phone with PSTN connectivity through Direct Routing, Operator Connect, and Teams Calling Plans. The Microsoft Teams Voice Engineer manages Teams-certified devices, audio/video conferencing, and voice migration. The Microsoft Teams Voice Engineer collaborates with telephony providers and third-party vendors to enable advanced voice features in Microsoft Teams. The Microsoft Teams Voice Engineer also works with administrators for other workloads, including networking, identity, licensing, security, and compliance. To earn the Microsoft Teams Voice Engineer certification, candidates must pass Exam MS-700: Managing Microsoft Teams in addition to the MS-720 exam.</p>
            
