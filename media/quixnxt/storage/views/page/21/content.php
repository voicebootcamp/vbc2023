              </a>
              <div class="qx-accordion-content" hidden aria-hidden="true">
                <ul>
<li>Working knowledge of fundamental terms and concepts of computer networking, including LANs, WANs, and IP switching and routing&nbsp;</li>
<li>Basic knowledge of CUCM</li>
<li>Basic knowledge of contact center operations&nbsp;</li>
<li>Familiarity with Microsoft desktop applications and SQL database operations</li>
